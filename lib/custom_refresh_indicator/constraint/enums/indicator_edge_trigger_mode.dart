///
/// [IndicatorEdgeTriggerMode] is used to determine the trigger mode of the indicator.
///
/// [onEdge] means that the indicator will be triggered when the indicator is on the edge of the screen.
///
/// [anyWhere] means that the indicator will be triggered when the indicator is anywhere on the screen.
///

enum IndicatorEdgeTriggerMode { onEdge, anyWhere }
